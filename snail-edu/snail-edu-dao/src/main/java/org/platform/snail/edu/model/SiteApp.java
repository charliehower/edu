package org.platform.snail.edu.model;

import java.util.Date;

public class SiteApp {
	private String id;

	private String title;

	private String category;

	private String userId;

	private Date appTime;

	private Date start;

	private Date end;

	private Date createTime;

	private String content;

	private String className;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Date getAppTime() {
		return appTime;
	}

	public void setAppTime(Date appTime) {
		this.appTime = appTime;
	}

	public Date getStart() {
		return start;
	}

	public void setStart(Date start) {
		this.start = start;
	}

	public Date getEnd() {
		return end;
	}

	public void setEnd(Date end) {
		this.end = end;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	@Override
	public String toString() {
		return "SiteApp [id=" + id + ", title=" + title + ", category="
				+ category + ", userId=" + userId + ", appTime=" + appTime
				+ ", start=" + start + ", end=" + end + ", createTime="
				+ createTime + ", content=" + content + ", className="
				+ className + "]";
	}


}